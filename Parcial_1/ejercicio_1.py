#1 Haciendo uso de los comentarios defina los siguientes conceptos:

#Clase: En programación orientada a objetos, una "clase" es una plantilla o un conjunto de instrucciones 
#       que define la estructura y características comunes de un grupo de objetos.

#Objeto: Es una instancia de una clase que representa entidades del mundo real con sus atributos y comportamientos. 

#Self: Es el primer parámetro de cualquier método y hace referencia al propio objeto con el que se llamó el método 
#      para acceder a sus atributos

#Constructor:Es un método especial que inicializa todos los atributos de una clase.

#Programación orientada a objetos: La programación orientada a objetos es un paradigma de programación 
#             que se basa en el concepto de objetos, adoptando la naturaleza de un objeto en la vida real, 
#             el cual tiene un comportamiento y unos atributo.

#Métodos: Son funciones internas de una clase que represental el comportamiento de un objeto.

#Instancia: Es un un objeto específico creado a partir de una clase.
 